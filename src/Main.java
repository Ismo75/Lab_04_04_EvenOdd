public class Main {
    public static void main(String[] args)
    {
        int intNumToExamine = 2;
        // Use modulo to check even or odd
        if (intNumToExamine % 2 == 0) {
            System.out.println(intNumToExamine + " is Even.");
        } else {
            System.out.println(intNumToExamine + " is Odd.");
        }
    }
}